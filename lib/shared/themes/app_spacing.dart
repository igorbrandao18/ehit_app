// shared/themes/app_spacing.dart
class AppSpacing {
  // Base spacing unit
  static const double base = 8.0;
  
  // Spacing scale
  static const double xs = base * 0.5; // 4
  static const double sm = base; // 8
  static const double md = base * 1.5; // 12
  static const double lg = base * 2; // 16
  static const double xl = base * 3; // 24
  static const double xxl = base * 4; // 32
  static const double xxxl = base * 6; // 48
  
  // Specific spacing for music app
  static const double playerPadding = 24;
  static const double cardPadding = 16;
  static const double listItemPadding = 12;
  static const double sectionSpacing = 32;
  static const double albumArtSpacing = 16;
}
